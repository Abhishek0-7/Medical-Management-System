        // TODO add your handling code here:
        
        String Exists = jTextField6.getText();
        String Name = jTextField3.getText();
        String Function = jTextField5.getText();
        String Units = jTextField4.getText();
        String Cost = jTextField2.getText();
        String Reason= jTextField1.getText();
    
        
        
        //Establish Dtabase Connection
        Connection connection=null;
        Statement s;
        try {
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/macmandb", "root", "aditya");
            System.out.println("connected to db");
            
            s = connection.createStatement();
            
            //run sql query
            String addquery = "INSERT INTO Request_Equipment (name, equipment_function, units_to_be_allocated, already_exists, estimated_cost, reason) " +
             "VALUES ('" + Name + "', '" + Function + "', '" +Units+ "', '" + Exists + "', '" + Cost + "', '" + Reason + "');";

            s.executeUpdate(addquery);
            System.out.println("Record Inserted Successfully");
            connection.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(RequestEquipement.class.getName()).log(Level.SEVERE, null, ex);
        }
